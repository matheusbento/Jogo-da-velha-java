    public void sortearJogador(){
    Random rand = new Random();
        int num = rand.nextInt(1)+1;
        if(num==1){
        jogador = jogo.jogador1;
        }else{
        jogador = jogo.jogador2;
        }
    
    }
    public void mudarPeca(){
            if(jogador==jogo.jogador2){
                jogador=jogo.jogador1;
                mensagens(3);
            }else{
                jogador=jogo.jogador2;
                mensagens(3);
            }
            
    }
