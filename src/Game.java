    public void mensagens(int retorno){
        String mensagem = "\n" + dateFormat.format(Calendar.getInstance().getTime())+" - ";
        if(retorno == -1){
               mensagem+= "Já jogaram nessa posição";
        }else if(retorno == 3){
                mensagem+= "Vez do player "+ jogador.getNome();
        
        }else if(retorno == 40){
                mensagem+= "O Jogador  "+ jogador.getNome() + " GANHOU";
        botoes(false);
        }
        else if(retorno == 5){
                mensagem+= "O Jogo começou";
        }
        
        else{
            mensagem+= "Jogada feito com sucesso pelo Jogador " + jogador.getNome() + " utilizando a peça " + jogador.getSimbolo();
        }
        this.relatorio.append(mensagem);
    }
    private void btn11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn11ActionPerformed
        int returno = jogo.Jogar(0, 0, jogador);
        mensagens(returno);
        this.btn11.setText(""+jogo.matrix[0][0].getSimbolo());
        if(jogador==jogo.jogador1 && returno!=-1){
            this.btn11.setBackground(Color.BLACK);  
            this.btn11.setForeground(Color.WHITE);  
            
        }else if(jogador==jogo.jogador2 && returno!=-1){
            this.btn11.setBackground(Color.WHITE);  
            this.btn11.setForeground(Color.BLACK);  
        }
        if(returno!=-1){
        mudarPeca();
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btn11ActionPerformed

